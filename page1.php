        <h2 class="title_h2">Предисловие</h2>
          <div class="p_enter"><p> В английском языке существуют такие понятия как “Фразовые глаголы” и “Устойчивые выражения”. Английская речь наполнена этими компонентами, и для того, чтобы понимать носителей языка лучше, необходимо их знать.</p>
            

    <p>Что такое фразовые глаголы?</p>

        <p><b>Фразовый глагол</b> это сочетание глагола и предлога, которые можно делить при написании или произношении в необходимых для этого ситуациях. Этот элемент языка поможет выкутаться практически из любой затруднённой ситуации, посредством передачи нужно информации, без необходимости употребления каких-либо сложных оборотов и/или слов. К примеру: Write down - записать (например, в тетрадь).</p>

          
    <p>Что такое устойчивые выражения?</p> 
          
        <p><b>Устойчивые выражения</b>  - это некие конструкции, части из которых ни в коем случае нельзя делить и менять местами. Представителями обычно являются вводные фразы, или идиомы. Используются в основном для украшения речи или написания эссе (вводные фразы). Но также дают возможность выглядеть в глазах нативных иностранцев более толково.</p>
      
      </div>
