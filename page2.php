        <h2 class="title_h2">Введение во времена</h2>
          <div class="p_enter">
              
    <p>Прежде всего, стоит отметить, что нет необходимости идеально учить и употреблять все времена.  
    Но знать надо даже самые редкие, чтобы не попасть впросак при слуху или видении такой вещи. </p>
    <p> По употреблению можно расположить времена следующим образом (от самого популярняго к менее)</p>
    <p>Present Simple, Past Simple, Future Simple, Present Continuous, Present Pefect Continuous, Present Perfect, Past Continuous, Past Perfect, Future Continuous, Future Perfect, Past Pefect Continuous,  
    Future Perfect Continuous (зачастую не употребляется даже носителями). </p>

    <p>Для самого минимума можно знать всего 6 времен (все Simple, Present continuous, Present Perfect, Present Perfect Continuous)</p>
      </div>
          <div class="wrap_time">
        <div class="time_1">
            <br>
            <p class="p_title">Present simple (Действие происходит  в неопределённый момент времени) </p>
            <br>
            <p class="plus">Positive(+)</p> 
            <p class="ptime">I/we/you/they + verb  (Example: I work - Я работаю)</p>
            <p class="ptime">He/she/it + (verb+s) (Example: He plays - Он играет)</p>     

            <p class="minus">Negative(-)</p> 
            <p class="ptime">I/we/you/they + do not (don’t) + verb (Example: We don’t buy it - Мы не покупаем это)</p>
            <p class="ptime">He/she/it + does not(doesn’t) + verb (Example: She doesn’t cook - Она не готовит)</p>

            <p class="quest">Question(?)</p>
            <p class="ptime">Do + I/we/you/they + verb (Example: Do you know it? - Ты знаешь это?)</p>
            <p class="ptime">Does + He/she/it  + verb (Example: Does it look strange? - Это выглядит странным?)</p>
         <br>
          </div>
              <br><br>
        <div class="time_2">
            <br>
            <p class="p_title">Present continuous  (Действие происходит  в настоящий момент времени) </p>
            <br>
            <p class="plus">Positive(+)<p> 
            <p class="ptime">I(am)/we(are)/you(are)/they(are) + (to be) +verb + ing </p>
            <p class="ptime">(Example: I am doing my homework - Я делаю свою домашнюю работу)</p> 
            <p class="ptime">(Example№2: They are writting - Они пишут)</p>
<p class="ptime">He(is)/she(is)/it(is) + (to be) + verb + ing (Example: He is reading a book - Он читает книгу)</p>     
<p class="minus">Negative(-) </p>
            
            <p class="ptime">I(am)/we(are)/you(are)/they(are) + (to be + not) +verb + ing </p>
   <p class="ptime"> (Example: They are not chatting with me - Они не болтают со мной) </p>
<p class="ptime">(Example№2: I am not thinking about it - Я не думаю об этом)</p>
<p class="ptime">He(is)/she(is)/it(is) + (to be + not) + verb + ing (Example: She is not watching TV - Она не смотрит телевизор)</p>     
<p class="quest">Question(?)</p>
<p class="ptime">Am I + verb + ing(Example: Am I doing it wrong? - Я делаю это неправильно?)</p>
<p class="ptime">Are we/you/they + verb + ing(Example: Are you kidding? - Ты что, шутишь?)</p>
<p class="ptime">Is + He/she/it  + verb + ing (Example: Is she walking? - Она гуляет?)</p>
            <br>
        </div>
               <br><br>
        <div class="time_3">
            <br>
            <p class="p_title">Present perfect (Действие произошло в прошлом, но имеет результат  в настоящем / опыт) </p>
            <br>
            <p class="plus">Positive(+) </p>
<p class="ptime">I/we/you/they + have + verb(3rd form) (Example: I have just come home - Я только что пришёл домой)</p>
<p class="ptime">He/she/it + has + verb(3rd form) (Example: It has already happened - Это уже произошло)</p>     

            <p class="minus">Negative(-) </p>
<p class="ptime">I/we/you/they + have not + verb(3rd form) (Example: I have not finished it yet  - Я ещё не закончил это)</p>
<p class="ptime">He/she/it + has not + verb(3rd form) (Example: He has not been there ever - Он не был там когда-либо)</p>     
    
            <p class="quest">Question(?)</p>
<p class="ptime">Have + I/we/you/they  + verb(3rd form) (Example: Have you ever made it? - Ты когда-нибудь делал это?)</p>
<p class="ptime">Has + He/she/it + verb(3rd form) (Example: Has it been announced? -Было ли это объявлено?)</p>  
            <br>
        </div>
               <br><br>
        <div class="time_4">
            <br>
            <p class="p_title">Present perfect continuous  (Действие началось в прошлом, но продолжается до сих пор) </p>
            <br>
            <p class="plus">Positive(+)</p> 
            <p class="ptime">I/we/you/they + have been +verb+ing </p>
    <p class="ptime">(Example: I have been learning English for 2 years - Я учу английский уже 2 года)</p> 
<p class="ptime">He/she/it + has been + verb+ing (Example: She has been waiting for me since morning - Она ждёт меня с утра)</p>      
            <p class="minus">Negative(-) (Может заменяться Present Perfect-ом)</p>
            <p class="ptime">I/we/you/they + have not been +verb+ing </p>
<p class="ptime">(Example: You haven’t been working there for 2 weeks  - Ты не работаешь там уже 2 недели)</p> 
            <p class="ptime">He/she/it + has not been + verb+ing</p>
<p class="ptime">(Example: He hasn’t been listening to her for a whole day - Он не слушал её весь день)</p>      
    
            <p class="quest">Question(?)</p>
<p class="ptime">Have + I/we/you/they + been + verb+ing </p> 
<p class="ptime">(Example: How long have they been talking to each other? - Как долго они разговаривают друг с другом?)</p> 
<p class="ptime">Has + He/she/it + been + verb+ing</p>  
<p class="ptime">(Example: How much time has he been refusing from an offer? - Сколько времени он отказывается от предложения?)</p> 
            <br>
        </div>
               <br><br>
        <div class="time_5">
            <br>
            <p class="p_title">Past simple (Действие произошло  в определенный момент времени в прошлом) </p>
            <br>
            <p class="plus">Positive(+)</p> 
<p class="ptime">I/we/you/they + verb(2nd form)  (Example: I went there - Я пошёл туда)</p>
<p class="ptime">He/she/it + verb(2nd form) (Example: He counted on me - Он рассчитывал на меня)</p>     

            <p class="minus">Negative(-) </p>
<p class="ptime">I/we/you/they + did not + verb (Example: We did not know these people 2 days ago - Мы не знали этих людей 2 дня назад)</p>
<p class="ptime">He/she/it + did not + verb (Example: She did not do it yesterday - Она этого не делала вчера)</p>

<p class="quest">Question(?)</p>
<p class="ptime">Did + I/we/you/they + verb (Example: Did you noticed it? - Ты заметил это?)</p>
<p class="ptime">Did + He/she/it  + verb (Example: Did it seem cool? - Это казалось крутым?)</p>
            <br>
        </div>
               <br><br>
        <div class="time_6">
            <br>
            <p class="p_title">Future simple (Действие произойдёт  в неопределенный момент времени в будущем) </p>
            <br>
            <p class="plus">Positive(+)</p> 
<p class="ptime">I/we/you/they + will + verb  (Example: I will buy it - Я куплю это)</p>
<p class="ptime">He/she/it + will + verb (Example: He will see the film - Он увидит этот фильм)</p>   

<p class="minus">Negative(-)</p> 
<p class="ptime">I/we/you/they + will not + verb (Example: We will not call them - Мы не позвоним им)</p>
<p class="ptime">He/she/it + will not + verb (Example: She won’t learn it by heart - Она не выучит это наизусть)</p>

            <p class="quest">Question(?)</p>
<p class="ptime">Will + I/we/you/they + verb (Example: Will you go there? - Ты пойдёшь туда?)</p>
<p class="ptime">Will + He/she/it  + verb (Example: Will it come to us? - Придёт ли оно к нам?)</p>
