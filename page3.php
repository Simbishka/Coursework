        <h2 class="title_h2">Всеми любимые, но не каждому дающиеся, английские слова..</h2>
          <div class="p_enter">
    <p>Ни для кого не секрет, что словарный запас в английском, это одна из самых главных составляющих. И далеко не всем они подвластны, кому-то из-за плохой памяти, кому-то из-за плохого произношения. </p>

<p><b>Первый совет,</b> учите правильное произношение, и учитесь читать транскрипции, которые являются ключом для всего английского языка. Даже если ваш уровень ниже среднего или выше, но вы не умеете правильно произносить элементарные слова, то стоит акцентировать на этом внимание, даже если такое решение покажется смешным.</p>

