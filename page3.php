<p><b>Третий совет,</b> не зубрите слова, язык - это не какое-нибудь обществознание. Язык - это искусство. Он учится плавно и неспеша.</p> 

<p><b>Четвертый совет,</b> находите для себя интересные сферы, которые повествуются на английском языке. Это позволит вам узнать что-то новое и заодно подтянуть не только словарный запас, но и понимание иностранцев, общающихся на английском.</p>
      
